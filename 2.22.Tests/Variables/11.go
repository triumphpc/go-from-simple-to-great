package main

import "fmt"

func main() {
	s1, s2 := "String one", "String two"
	var s3, s4 string = "String third", "String forth"
	fmt.Println(s1, s2, s3, s4)

	var i1, i2, i3 int = 1, 2, 3
	fmt.Println(i1, i2, i3)

}
